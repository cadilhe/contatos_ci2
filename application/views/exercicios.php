    <h1 class="page-header">Exercicios</h1>

    <h3>Criar uma agenda telefônica(Crud completo)</h3>

    <p>Essa agenda deverá ser a mais completa possível.</p>

    <h4>Funcionalidades:</h4>
    <p><strong>Cadastro de clientes</strong>
    <ul>

        <li>Nome</li>
        <li>Sobrenome</li>
        <li>Endereço</li>
        <ul>
            <li>CEP</li>
            <li>rua</li>
            <li>bairro</li>
            <li>cidade</li>
            <li>estado</li>
            <li>país</li>
        </ul>
        <li>Telefones</li>
    </ul>

    <p><strong>Busca de clientes</strong>
    <ul>
        <li>Por nome
        <li>Por telefone
        <li>Por endereço
    </ul>

    <p><strong>OBS:</strong>O usuário pode ter 1 ou n endereços, tal como telefone.</p>

    <p><strong>As rotas devem ser:</strong>

    <ul>
        <li>seuprojeto/contatos</li>
        <li>seuprojeto/contato/ver/(:num)</li>
        <li>seuprojeto/contato/cadastrar</li>
        <li>seuprojeto/contato/editar/(:num)</li>
        <li>seuprojeto/contato/deletar/(:num)</li>
        <li>seuprojeto/buscar</li>
    </ul>


    <h3>Criar um sistema de login com: </h3>

    <p>Um <strong>formulário simples para cadastro</strong> com:

    <ul>
        <li>nome</li>
        <li>sobrenome</li>
        <li>email</li>
        <li>senha</li>
        <li>digitar senha novamente</li>
        <li>botão de cadastrar</li>
    </ul>

    <p><strong>O formulário de login:</strong>
    <ul>
        <li>email</li>
        <li>senha</li>
        <li>botão de logar</li>
    </ul>

    <p><strong>As rotas devem ser:</strong>

    <ul>
        <li>usuario/cadastrar</li>
        <li>usuario/login</li>
        <li>usuario/logout</li>
    </ul>

